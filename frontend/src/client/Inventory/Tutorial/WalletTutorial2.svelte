<div class="modal__info--tutorial" style="text-align: justify; line-height: 1.25">
  In States of Matter, players have access to two tokens: Etheric Crystals and
  Etheric Energy. Etheric Crystals, acquired through gameplay, serve multiple
  purposes such as crafting. Alternatively, they can be converted into Etheric
  Energy, the value of which increases gradually in terms of Etheric Crystals
  over time.
