<div class="modal__title" style="font-size: var(--xl)">Collectibles</div>
<div class="modal__info--tutorial" style="text-align: justify; line-height: 1.25">
  In the game, there are three types of collectibles: Avatars, Banners, and
  Skins, categorized into five rarities: Common, Uncommon, Rare, Epic, and
  Legendary. Common collectibles are available for free within the game,
  although some may require specific conditions to unlock. Others can be crafted
  using Etheric Crystals and shards obtained from Shard Packs or purchased from
  other players.
</div>
