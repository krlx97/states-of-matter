<div class="modal__title">Heroes</div>
<div class="modal__info--tutorial">
  In States of Matter, there are four heroes - one for each class, except for
  Neutral. You can only select one Hero per deck. The hero card is always on the game field and if it drops to 0
  health, the game ends. To play cards from your hand, you need to spend mana
  from your hero, which replenishes each turn. Additionally, each hero has a
  unique ability that can be used once per turn.
  <br/><br/>
