<div class="modal__info--tutorial" style="text-align: justify; line-height: 1.25">
  While hero cards are limited to one per deck, there are many minion, magic,
  and trap cards available in States of Matter. You can include up to 30 cards
  in a single deck, with a maximum of two copies of any given card. Minions can
  be placed onto the game field to attack other minions or heroes, while magic
  cards have an immediate effect when played. Trap cards are hidden until
  triggered by your opponent's actions, and only one can be set at a time.
